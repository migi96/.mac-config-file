export BAR_BORDER_COLOR=$BG2   # Bar border
export BACKGROUND_1=$BG1       # Background for items
export BACKGROUND_2=$BG2       # Secondary background
export ICON_COLOR=$WHITE       # Color of all icons
export LABEL_COLOR=$WHITE      # Color of all labels
export POPUP_BACKGROUND_COLOR=$BG1 # Popup background
export POPUP_BORDER_COLOR=$WHITE   # Popup border
export SHADOW_COLOR=$BLACK     # Shadow color
