#!/bin/bash

### Custom Greeny, Orangy, Yellowy Theme
export BLACK=0xff181818       # Dark background
export WHITE=0xfff0f0f0       # Light text/icons
export RED=0xffe74c3c         # Red for alerts
export GREEN=0xff2ecc71       # Vibrant green
export BLUE=0xff3498db        # Soft blue
export YELLOW=0xfff1c40f      # Bright yellow
export ORANGE=0xffe67e22      # Warm orange
export MAGENTA=0xff9b59b6     # Purple/magenta
export GREY=0xff95a5a6        # Neutral grey
export TRANSPARENT=0x00000000 # Fully transparent

# Background colors
export BG0=0xff1e1e1e         # Dark background (slightly lighter than BLACK)
export BG1=0xff2c2c2c         # Slightly lighter background
export BG2=0xff3a3a3a         # Even lighter background

# Battery colors (gradient from green to red)
export BATTERY_1=0xff2ecc71   # Green (full)
export BATTERY_2=0xfff1c40f   # Yellow (medium)
export BATTERY_3=0xffe67e22   # Orange (low)
export BATTERY_4=0xffe74c3c   # Red (very low)
export BATTERY_5=0xffc0392b   # Dark red (critical)

# General bar colors
